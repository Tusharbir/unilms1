    private void fetchallcourse() {
        System.out.println("func called");
        try {
            HttpResponse<String> response = Unirest.get("http://localhost:8080/adminviewcourse").asString();

            String ans = response.getBody();
            StringTokenizer st = new StringTokenizer(ans, "$$");

            al.clear();
            while (st.hasMoreTokens()) {
                String cdet = st.nextToken();
                StringTokenizer st2 = new StringTokenizer(cdet, ";;;");

                String cn = st2.nextToken();
                String dept = st2.nextToken();
                String des = st2.nextToken();
                String sem = st2.nextToken();
//               CourseDetails ob= new CourseDetails(cn, dept, des, sem);
//               al.add(ob);
                al.add(new CourseDetails(cn, dept, des, sem));

            }

            tm.fireTableDataChanged();
            //table model

        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    class mytablemodel extends AbstractTableModel {

        @Override
        public int getRowCount() {
            return al.size();
        }

        @Override
        public int getColumnCount() {
            return 4;
        }

        @Override
        public String getColumnName(int j) {
            String c[] = {"Course Name", "Department", "Semester", "Description"};
            return c[j];
        }

        @Override
        public Object getValueAt(int i, int j) {

            if (j == 0) {
                return al.get(i).cn;
            } else if (j == 1) {
                return al.get(i).dept;
            } else if (j == 2) {
                return al.get(i).sem;
            } else {
                return al.get(i).des;
            }

        }

    }

