    private void deletebtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletebtActionPerformed
        // TODO add your handling code here:

        int row = coursetb.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "No Row Selected");

        } else 
        {
            int sel = JOptionPane.showConfirmDialog(this, "Are You Sure You Want to Delete", "Uni LMS Delete Confirm Dialog Box", JOptionPane.YES_NO_OPTION, JOptionPane.PLAIN_MESSAGE, ic);
            if(sel==JOptionPane.YES_OPTION)
            {
            
            String course = al.get(row).cn;
            String se = al.get(row).sem;
            String de = al.get(row).dept;

//            System.out.println(row);
//            System.out.println(course);
//            System.out.println(se);
//            System.out.println(de);
            try {
                HttpResponse<String> response = Unirest.get("http://localhost:8080/admindeletecourse")
                        .queryString("course", course).queryString("depart", de).queryString("sem", se).asString();

                if (response.getStatus() == 200) {
                    String feedback3 = response.getBody();
                    if (feedback3.equals("Course Deleted !!!")) {
                        JOptionPane.showMessageDialog(this, "Course Deleted");
                        fetchallcourse();

                    } else {
                        System.out.println(response.getStatusText());
                    }
                }
            } catch (Exception ex) {
                ex.printStackTrace();
            }

        }
            else
            {
                JOptionPane.showMessageDialog(this, "Delete Cancelled");
            }
        }
    }//GEN-LAST:event_deletebtActionPerformed

