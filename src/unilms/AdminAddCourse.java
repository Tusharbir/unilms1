    private void cntfFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_cntfFocusLost
        // TODO add your handling code here:

        String coursen = cntf.getText().toUpperCase();

        if (coursen.equals("BTECH")) {
            smcb.removeAllItems();

            for (int i = 1; i <= 8; i++) {
                smcb.addItem(i + "");
            }

        } else if (coursen.startsWith("MTECH")) {
            smcb.removeAllItems();

            for (int i = 1; i <= 4; i++) {
                smcb.addItem(i + "");
            }
        } else if (coursen.startsWith("B") || coursen.startsWith("MCA")) {
            smcb.removeAllItems();

            for (int i = 1; i <= 6; i++) {
                smcb.addItem(i + "");
            }
        }
    }//GEN-LAST:event_cntfFocusLost

