    private void acbtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_acbtActionPerformed
        // TODO add your handling code here:

        new Thread(new Runnable() {
            @Override
            public void run() {

                String cname = cntf.getText();
                String dname = deptcb.getSelectedItem().toString();
                System.out.println(dname);

                String snum = smcb.getSelectedItem().toString();
                String desc = descta.getText();

                if (!cname.isEmpty() && !dname.isEmpty() && !snum.isEmpty() && !desc.isEmpty()) {
                    if (!dname.equals("Select Department")) {
                        try {
                            HttpResponse<String> response = Unirest.get("http://localhost:8080/adminaddcourse")
                                    .queryString("coursename", cname).queryString("department", dname).queryString("semester", snum).queryString("description", desc).asString();

                            if (response.getStatus() == 200) {
                                String feedback2 = response.getBody();
                                if (feedback2.equals("Course Added !!!")) {
//                                    JOptionPane.showMessageDialog(rootPane, "Course Added !!!");
                                    JOptionPane.showMessageDialog(rootPane, """
                                                                !!! Done !!!
                                                                Course Added""", "Uni LMS Admin Add Course", JOptionPane.PLAIN_MESSAGE, ic);
                                } else {
//                                    JOptionPane.showMessageDialog(rootPane, "Course Already Exists");
                                    JOptionPane.showMessageDialog(rootPane, """
                                                                !!! OOPS !!!
                                                                Course Already Exists""", "Uni LMS Admin Add Course", JOptionPane.PLAIN_MESSAGE, ic);
                                }

                            } else {
                                System.out.println(response.getStatusText());
                            }

                        } catch (Exception ex) {
                            ex.printStackTrace();
                        }
                    } else {
//                        JOptionPane.showMessageDialog(rootPane, "Select Department");
                                                            JOptionPane.showMessageDialog(rootPane, """
                                                                !!! OOPS !!!
                                                                Select Department""", "Uni LMS Admin Add Course", JOptionPane.PLAIN_MESSAGE, ic);
                    }

                } else {
//                    JOptionPane.showMessageDialog(rootPane, "Empty Field Exists");
                    JOptionPane.showMessageDialog(rootPane, """
                                                                !!! OOPS !!!
                                                                Empty Field Exists""", "Uni LMS Admin Add Course", JOptionPane.PLAIN_MESSAGE, ic);
                }

            }
        }).start();


    }//GEN-LAST:event_acbtActionPerformed

