    private void fetchallteacher() {
        String dept = deptcb.getSelectedItem().toString();
        try {
            HttpResponse<String> response = Unirest.get("http://localhost:8080/adminviewteacher").queryString("dept", dept).asString();

            String ans2 = response.getBody();

            System.out.println(ans2);
            StringTokenizer st = new StringTokenizer(ans2, "$$");

            al2.clear();
            while (st.hasMoreTokens()) {
                String cdet = st.nextToken();
                StringTokenizer st2 = new StringTokenizer(cdet, ";;;");

                String ti = st2.nextToken();
                String tn = st2.nextToken();
                String fn = st2.nextToken();
                String ad = st2.nextToken();
                String em = st2.nextToken();
                String pn = st2.nextToken();
                String qual = st2.nextToken();
                String de = st2.nextToken();
                String ph = st2.nextToken();

//               CourseDetails ob= new CourseDetails(cn, dept, des, sem);
//               al.add(ob);
                al2.add(new TeacherDetails(ti, tn, fn, ad, em, pn, qual, de, ph));

            }

            tm2.fireTableDataChanged();
            //table model

        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    class mytablemodel2 extends AbstractTableModel {

        @Override
        public int getRowCount() {
            return al2.size();
        }

        @Override
        public int getColumnCount() {
            return 8;
        }

        @Override
        public String getColumnName(int j) {
            String c[] = {"Teacher Name", "Father's Name", "Address", "Email_id", "Phone_Num", "Qualification", "Department", "Photo"};
            return c[j];
        }

        @Override
        public Object getValueAt(int i, int j) {

            if (j == 0) {
                return al2.get(i).tn;
            } else if (j == 1) {
                return al2.get(i).fn;
            } else if (j == 2) {
                return al2.get(i).ad;
            } else if (j == 3) {
                return al2.get(i).em;
            } else if (j == 4) {
                return al2.get(i).pn;
            } else if (j == 5) {
                return al2.get(i).qual;
            } else if (j == 6) {
                return al2.get(i).de;
            } else {
                return al2.get(i).ph;
            }

        }

    }

