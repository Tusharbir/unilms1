    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        

        int row = studenttb.getSelectedRow();
        if(row==-1)
        {
            JOptionPane.showMessageDialog(this, "No Row Selected");
        }
        else
        {
            int sel = JOptionPane.showConfirmDialog(this, "Are You Sure You Want to Delete", "Uni LMS Delete Confirm Dialog Box", JOptionPane.YES_NO_OPTION);
            if(sel==JOptionPane.YES_OPTION)
            {

            String si = al2.get(row).si;

            try
            {
                HttpResponse<String> response = Unirest.get("http://localhost:8080/admindeletestudent")
                .queryString("studentid",si).asString();

                if(response.getStatus()==200)
                {
                    String feedback4 = response.getBody();
                    if(feedback4.equals("Student Deleted !!!"))
                    {

                        fetchallstudent();
//                        JOptionPane.showMessageDialog(this, "Student Deleted !!!");
                        JOptionPane.showMessageDialog(this, """
                                                                !!! Done !!!
                                                                Student Record Deleted""", "Uni LMS Manage Student", JOptionPane.PLAIN_MESSAGE, ic);

                    }

                    else{
                        System.out.println(response.getStatusText());
                    }
                }
            }

            catch(Exception ex)
            {
                ex.printStackTrace();
            }
        }
            else
            {
                JOptionPane.showMessageDialog(this, """
                                                                !!! OOPS !!!
                                                                Operation Cancelled""", "Uni LMS Manage Student", JOptionPane.PLAIN_MESSAGE, ic);
            }
    }
        
    }//GEN-LAST:event_jButton3ActionPerformed

