    private void fetchallstudent()
    {
        String dept = deptcb.getSelectedItem().toString();
        try {
            HttpResponse<String> response = Unirest.get("http://localhost:8080/adminviewstudent").queryString("dept", dept).asString();

            String ans10 = response.getBody();
            
            System.out.println(ans10);
            StringTokenizer st = new StringTokenizer(ans10, "$$");
            
            al2.clear();
            while (st.hasMoreTokens()) {
                String sdet = st.nextToken();
                StringTokenizer st2 = new StringTokenizer(sdet, ";;;");
                
                String si = st2.nextToken();
                String sn = st2.nextToken();
                String fn = st2.nextToken();
                String mn = st2.nextToken();

                String add = st2.nextToken();
                String em = st2.nextToken();
                                String mob = st2.nextToken();

                String depart = st2.nextToken();
                String sem = st2.nextToken();
                String course = st2.nextToken();
                String photo = st2.nextToken();

                

//               CourseDetails ob= new CourseDetails(cn, dept, des, sem);
//               al.add(ob);
               al2.add(new Student(si,sn,fn,mn,add,em,depart,sem,course,mob,photo));
                
                

            }
            
            tm2.fireTableDataChanged();
            //table model
            
            

        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
    
    class mytablemodel2 extends AbstractTableModel
    {
        @Override
        public int getRowCount() 
        {
           return al2.size();
        }

        @Override
        public int getColumnCount() 
        {
           return 8;
        }

        @Override
        public String getColumnName(int j)
        {
             String c[] = { "Student Name","Father's Name","Mother's Name","Address","Email_id", 
                 "Department", "Semester", "Course", "Mobile No." };
             return c[j];
        }
        
        @Override
        public Object getValueAt(int i, int j)
        {
           
            
            if(j==0) 
                return al2.get(i).sn;
            else if(j==1)
                return al2.get(i).fn;
            else if(j==2)
                return al2.get(i).mn;
            else if(j==3)
                return al2.get(i).add;
            else if(j==4)
                return al2.get(i).em;
            else if(j==5)
                return al2.get(i).dept;
            else if(j==6)
                return al2.get(i).sem;
            else if(j==7)
                return al2.get(i).course;   
            else
                return al2.get(i).mob;
            
        }
    }

